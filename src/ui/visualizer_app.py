def has_template(value: str) -> bool:
    """Return True when a string contains the period placeholder."""
    return "{period}" in (value or "") or "{periodo}" in (value or "")


def normalize_template(value: str) -> str:
    """Normalize the legacy period placeholder name to `{period}`."""
    return (value or "").replace("{periodo}", "{period}")


def coerce_template(user_value: str, env_value: str, fallback_template: str) -> str:
    """Select the most appropriate template or explicit path."""
    user_value = normalize_template(user_value)
    env_value = normalize_template(env_value)
    fallback_template = normalize_template(fallback_template)

    if has_template(user_value):
        return user_value
    if has_template(env_value):
        return env_value
    if user_value and user_value.strip():
        return user_value
    if env_value and env_value.strip():
        return env_value
    return fallback_template


def render_template(template: str, period: str) -> str:
    """Render a period-aware template."""
    return normalize_template(template).format(period=period)


def df_to_bytes(df: pd.DataFrame, kind: str = "csv") -> Optional[bytes]:
    """Serialize a DataFrame for Streamlit download buttons."""
    if df is None:
        return None

    if kind == "csv":
        return df.to_csv(index=False).encode("utf-8")

    buffer = io.BytesIO()
    with pd.ExcelWriter(buffer, engine="openpyxl") as writer:
        df.to_excel(writer, index=False, sheet_name="report")
    return buffer.getvalue()


def strip_accents_lower(value: str) -> str:
    """Normalize text by removing accents and converting to lowercase."""
    value = str(value or "")
    normalized = unicodedata.normalize("NFKD", value)
    normalized = "".join(
        character for character in normalized if not unicodedata.combining(character)
    )
    return normalized.lower()


def safe_slug(value: str) -> str:
    """Return a filesystem-safe slug."""
    value = str(value or "").strip()
    value = re.sub(r"[^A-Za-z0-9_\-]+", "_", value)
    return value.strip("_") or "file"


def _pick_col(df: pd.DataFrame, candidates: List[str]) -> str:
    """Return the first matching DataFrame column from a candidate list."""
    columns = {str(column).strip().lower(): column for column in df.columns}
    for candidate in candidates:
        if candidate.lower() in columns:
            return columns[candidate.lower()]
    return ""


def _join_unique(values: Iterable[Any]) -> str:
    """Join unique non-empty values as a comma-separated string."""
    clean_values = []
    for value in values:
        if pd.isna(value):
            continue
        text = str(value).strip()
        if not text or text.lower() in {"nan", "none", "null"}:
            continue
        clean_values.append(text)
    return ", ".join(sorted(set(clean_values)))


def _clean_room_series(series: pd.Series) -> pd.Series:
    """Normalize room codes while preserving empty-room markers as blanks."""
    if series.empty:
        return series

    empty_markers = {
        "",
        "SIN_AULA",
        "SIN AULA",
        "NO_ROOM",
        "NA",
        "N/A",
        "NULL",
        "NONE",
    }

    return (
        series.fillna("")
        .astype(str)
        .str.strip()
        .str.upper()
        .replace({marker: "" for marker in empty_markers})
    )


def _normalize_teacher_key(value: Any) -> str:
    """Normalize teacher identifiers."""
    text = str(value or "").strip().upper()
    text = re.sub(r"\s+", "", text)
    return text


def normalize_day(value: Any) -> str:
    """Normalize day labels to the compact convention used by the model."""
    text = str(value or "").strip().upper()
    text_no_accents = strip_accents_lower(text).upper()
    mapping = {
        "MONDAY": "L",
        "LUNES": "L",
        "L": "L",
        "TUESDAY": "M",
        "MARTES": "M",
        "M": "M",
        "WEDNESDAY": "X",
        "MIERCOLES": "X",
        "MIÉRCOLES": "X",
        "X": "X",
        "THURSDAY": "J",
        "JUEVES": "J",
        "J": "J",
        "FRIDAY": "V",
        "VIERNES": "V",
        "V": "V",
        "SATURDAY": "S",
        "SABADO": "S",
        "SÁBADO": "S",
        "S": "S",
        "SUNDAY": "D",
        "DOMINGO": "D",
        "D": "D",
    }
    return mapping.get(text, mapping.get(text_no_accents, text[:1]))


