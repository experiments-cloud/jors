@st.cache_data(show_spinner=False)
def load_json(path: str) -> dict:
    """Load a JSON file if it exists."""
    if not path or not os.path.isfile(path):
        return {}

    with open(path, "r", encoding="utf-8") as file:
        return json.load(file)


@st.cache_data(show_spinner=False)
def load_csv(path: str) -> pd.DataFrame:
    """Load a CSV file as strings and return an empty DataFrame when missing."""
    if not path or not os.path.isfile(path):
        return pd.DataFrame()

    try:
        return pd.read_csv(path, dtype=str).fillna("")
    except Exception:
        return pd.DataFrame()


@st.cache_data(show_spinner=False)
def load_first_existing(paths: List[str]) -> pd.DataFrame:
    """Load the first non-empty CSV file found from a candidate list."""
    for path in paths:
        df = load_csv(path)
        if df is not None and not df.empty:
            return df
    return pd.DataFrame()


@st.cache_data(show_spinner=False)
def load_bundle(prefix: str) -> Dict[str, pd.DataFrame]:
    """Load teacher, room, and timetable outputs for one solution prefix."""
    teachers = load_first_existing(
        [prefix + suffix for suffix in _candidate_suffixes("teachers")]
    )
    rooms = load_first_existing(
        [prefix + suffix for suffix in _candidate_suffixes("rooms")]
    )
    timetable = load_first_existing(
        [prefix + suffix for suffix in _candidate_suffixes("timetable")]
    )

    if not timetable.empty:
        if "aula" in timetable.columns:
            timetable["aula"] = _clean_room_series(timetable["aula"])
        if "dia" in timetable.columns:
            timetable["dia"] = timetable["dia"].apply(normalize_day)
        if "hora" in timetable.columns:
            timetable["hora"] = timetable["hora"].astype(str).str.strip()

    if not rooms.empty and "aula" in rooms.columns:
        rooms["aula"] = _clean_room_series(rooms["aula"])

    return {"teachers": teachers, "rooms": rooms, "timetable": timetable}


@st.cache_data(show_spinner=False)
def load_teacher_catalog() -> pd.DataFrame:
    """Load an optional teacher catalog used to display teacher names."""
    if not TEACHER_CATALOG_CSV or not os.path.isfile(TEACHER_CATALOG_CSV):
        return pd.DataFrame(columns=["profesor", "profesor_nombre"])

    df = pd.read_csv(TEACHER_CATALOG_CSV, dtype=str).fillna("")
    df.columns = [str(column).strip().lower() for column in df.columns]

    teacher_id_col = _pick_col(df, ["rfc", "profesor", "docente", "clave", "teacher"])
    teacher_name_col = _pick_col(
        df,
        [
            "nombre_completo",
            "nombre",
            "docente_nombre",
            "profesor_nombre",
            "teacher_name",
        ],
    )

    if not teacher_id_col or not teacher_name_col:
        return pd.DataFrame(columns=["profesor", "profesor_nombre"])

    out = df[[teacher_id_col, teacher_name_col]].copy()
    out.columns = ["profesor", "profesor_nombre"]
    out["profesor"] = out["profesor"].apply(_normalize_teacher_key)
    out["profesor_nombre"] = out["profesor_nombre"].astype(str).str.strip()
    out = out[(out["profesor"] != "") & (out["profesor_nombre"] != "")]
    out = out.drop_duplicates("profesor")

    return out


