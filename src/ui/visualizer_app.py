"""
Visualizer 
==============

Streamlit dashboard for inspecting and validating academic timetabling
solutions generated by the MILP solver.

The dashboard reads anonymized JSON instances and representative CSV outputs,
then provides visual checks for timetable assignments, room utilization,
teacher workload, fixed-room consistency, and solution comparisons.

Main responsibilities
---------------------
- Load anonymized model instances and solver outputs.
- Display timetable assignments.
- Analyze room utilization.
- Analyze teacher and course workload.
- Check fixed-room consistency.
- Compare two solution outputs when available.

Notes
-----
Some internal data-column names such as `aula`, `profesor`, `materia`, and
`grupo` are preserved for compatibility with solver-generated CSV files.
"""

