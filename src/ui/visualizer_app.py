def get_alias_map(meta: dict) -> Dict[str, str]:
    """Return the room-alias map from metadata and environment variables."""
    alias_map: Dict[str, str] = {}

    if meta and meta.get("LAB_ALIAS_MAP"):
        alias_map.update(
            {str(key).upper(): str(value) for key, value in meta["LAB_ALIAS_MAP"].items()}
        )

    for alias, real_room in LAB_ALIAS_MAP_ENV.items():
        if real_room:
            alias_map[str(real_room).upper()] = str(alias)

    return alias_map


def get_course_name_map(meta: dict) -> Dict[str, str]:
    """Return a course-code to course-name map."""
    output: Dict[str, str] = {}
    raw = meta.get("M_text", {}) or meta.get("m_text", {}) or {}

    if isinstance(raw, dict):
        for key, value in raw.items():
            text = str(value or "").strip()
            if text:
                text = text.split("|")[0].strip()
            output[str(key).upper()] = text

    return output


def add_alias_column(
    df: pd.DataFrame,
    alias_map: Dict[str, str],
    room_col: str = "aula",
) -> pd.DataFrame:
    """Add a visual room-alias column when possible."""
    if df is None or df.empty or room_col not in df.columns:
        return df

    out = df.copy()
    out["aula_alias"] = (
        out[room_col].astype(str).str.upper().map(alias_map).fillna("")
    )
    return out


def enrich_timetable_labels(
    timetable: pd.DataFrame,
    meta: dict,
    teacher_catalog: pd.DataFrame,
) -> pd.DataFrame:
    """Add readable course and teacher names to a timetable DataFrame."""
    if timetable.empty:
        return timetable

    out = timetable.copy()
    course_name_map = get_course_name_map(meta)

    if "materia" in out.columns:
        out["materia_nombre"] = (
            out["materia"].astype(str).str.upper().map(course_name_map).fillna("")
        )

    if "profesor" in out.columns:
        out["profesor"] = out["profesor"].apply(_normalize_teacher_key)
        if not teacher_catalog.empty:
            out = out.merge(teacher_catalog, on="profesor", how="left")
            out["profesor_nombre"] = (
                out["profesor_nombre"].fillna("").astype(str).str.strip()
            )
        else:
            out["profesor_nombre"] = ""

    return out


def derive_course_room_from_timetable(timetable: pd.DataFrame) -> pd.DataFrame:
    """Derive one representative room per course-group pair from the timetable."""
    required_cols = {"materia", "grupo", "aula"}
    if timetable.empty or not required_cols.issubset(timetable.columns):
        return pd.DataFrame(columns=["materia", "grupo", "aula", "room_mode"])

    view = timetable.copy()
    view["aula"] = _clean_room_series(view["aula"])
    view = view[view["aula"] != ""]

    if view.empty:
        return pd.DataFrame(columns=["materia", "grupo", "aula", "room_mode"])

    counts = (
        view.groupby(["materia", "grupo", "aula"])
        .size()
        .reset_index(name="sesiones")
    )
    top = (
        counts.sort_values(
            ["materia", "grupo", "sesiones"],
            ascending=[True, True, False],
        )
        .drop_duplicates(["materia", "grupo"])
    )
    distinct = (
        counts.groupby(["materia", "grupo"])["aula"]
        .nunique()
        .reset_index(name="room_count")
    )
    out = top.merge(distinct, on=["materia", "grupo"], how="left")
    out["room_mode"] = out["room_count"].apply(
        lambda value: "SINGLE" if int(value) == 1 else "MULTIPLE"
    )

    return out[["materia", "grupo", "aula", "room_mode"]]


def build_course_assignments(
    teacher_df: pd.DataFrame,
    room_df: pd.DataFrame,
    timetable: pd.DataFrame,
    prefer_timetable: bool = True,
) -> pd.DataFrame:
    """Build a course-level assignment table by merging teacher and room data."""
    if prefer_timetable and (room_df.empty or "aula" not in room_df.columns):
        room_df = derive_course_room_from_timetable(timetable)
    elif prefer_timetable and not timetable.empty:
        derived = derive_course_room_from_timetable(timetable)
        if not derived.empty:
            if not room_df.empty:
                room_df = room_df.merge(
                    derived[["materia", "grupo", "room_mode"]],
                    on=["materia", "grupo"],
                    how="left",
                )
            else:
                room_df = derived

    if teacher_df.empty and room_df.empty:
        return pd.DataFrame()

    keys = ["materia", "grupo"]

    if "periodo" in teacher_df.columns and "periodo" in room_df.columns:
        keys.append("periodo")
    elif "periodo" in teacher_df.columns:
        room_df = room_df.copy()
        room_df["periodo"] = ""
    elif "periodo" in room_df.columns:
        teacher_df = teacher_df.copy()
        teacher_df["periodo"] = ""

    if teacher_df.empty:
        base = room_df.copy()
    elif room_df.empty:
        base = teacher_df.copy()
    else:
        base = pd.merge(teacher_df, room_df, on=keys, how="outer")

    return base.fillna("")


def derive_day_hour_sets(meta: dict, timetable: pd.DataFrame) -> tuple[List[str], List[str]]:
    """Derive day and hour sets from metadata or timetable data."""
    days = [str(item) for item in meta.get("D", [])] if meta else []
    hours = [str(item) for item in meta.get("H", [])] if meta else []

    if days and hours:
        return days, hours

    if not timetable.empty:
        days = (
            sorted(
                timetable["dia"].astype(str).unique().tolist(),
                key=lambda item: DAY_ORDER.get(str(item), 99),
            )
            if "dia" in timetable.columns
            else ["L", "M", "X", "J", "V"]
        )
        hours = (
            sorted(
                timetable["hora"].astype(str).unique().tolist(),
                key=lambda item: (len(item), item),
            )
            if "hora" in timetable.columns
            else []
        )
        return days, hours

    return ["L", "M", "X", "J", "V"], [f"{hour:02d}" for hour in range(7, 22)]


def build_room_catalog(meta: dict, alias_map: Dict[str, str]) -> pd.DataFrame:
    """Build the full allowed-room catalog from metadata."""
    rooms = [str(item).upper() for item in (meta.get("A", []) or [])]
    theory_rooms = set(str(item).upper() for item in (meta.get("AT", []) or []))
    lab_rooms = set(str(item).upper() for item in (meta.get("AL", []) or []))
    room_capacity = {
        str(key).upper(): value for key, value in (meta.get("cap_A", {}) or {}).items()
    }

    if not rooms:
        rooms = sorted(set(AT_HARD) | set(AL_HARD))
        theory_rooms = set(AT_HARD)
        lab_rooms = set(AL_HARD)

    rows = []
    for room in rooms:
        if room in lab_rooms:
            room_type = "LAB"
        elif room in theory_rooms:
            room_type = "THEORY"
        else:
            room_type = "OTHER"

        rows.append(
            {
                "aula": room,
                "tipo": room_type,
                "capacidad": room_capacity.get(room, ""),
                "aula_alias": alias_map.get(room, ""),
            }
        )

    return pd.DataFrame(rows).sort_values(["tipo", "aula"]).reset_index(drop=True)


def build_utilization(
    timetable: pd.DataFrame,
    meta: dict,
    alias_map: Dict[str, str],
) -> pd.DataFrame:
    """Build a room-utilization table including unused rooms."""
    days, hours = derive_day_hour_sets(meta, timetable)
    weekly_capacity = len(days) * len(hours)
    catalog = build_room_catalog(meta, alias_map)

    if catalog.empty:
        return pd.DataFrame(
            columns=[
                "aula",
                "tipo",
                "aula_alias",
                "capacidad",
                "sesiones",
                "cap_bloques",
                "utilizacion_%",
                "huecos_libres",
                "estado_uso",
            ]
        )

    if timetable.empty or "aula" not in timetable.columns:
        aggregated = pd.DataFrame(columns=["aula", "sesiones"])
    else:
        view = timetable.copy()
        view["aula"] = _clean_room_series(view["aula"])
        view = view[view["aula"] != ""]
        aggregated = (
            view.groupby("aula").size().reset_index(name="sesiones")
            if not view.empty
            else pd.DataFrame(columns=["aula", "sesiones"])
        )

    out = catalog.merge(aggregated, on="aula", how="left")
    out["sesiones"] = out["sesiones"].fillna(0).astype(int)
    out["cap_bloques"] = weekly_capacity
    out["utilizacion_%"] = (out["sesiones"] / weekly_capacity * 100).round(1)
    out["huecos_libres"] = weekly_capacity - out["sesiones"]
    out["estado_uso"] = out["sesiones"].apply(
        lambda value: "USED" if int(value) > 0 else "UNUSED"
    )

    return out.sort_values(
        ["sesiones", "tipo", "aula"],
        ascending=[False, True, True],
    ).reset_index(drop=True)


def detect_room_overlaps(timetable: pd.DataFrame) -> pd.DataFrame:
    """Detect room overlaps by room, day, and hour."""
    if timetable.empty or not {"aula", "dia", "hora"}.issubset(timetable.columns):
        return pd.DataFrame(columns=["aula", "dia", "hora", "count"])

    cols = ["aula", "dia", "hora"]
    if "periodo" in timetable.columns:
        cols = ["periodo"] + cols

    duplicated = timetable.groupby(cols).size().reset_index(name="count")
    return duplicated[duplicated["count"] > 1].sort_values(cols)


def detect_teacher_overlaps(timetable: pd.DataFrame) -> pd.DataFrame:
    """Detect teacher overlaps by teacher, day, and hour."""
    if timetable.empty or not {"profesor", "dia", "hora"}.issubset(timetable.columns):
        return pd.DataFrame(columns=["profesor", "dia", "hora", "count"])

    cols = ["profesor", "dia", "hora"]
    if "periodo" in timetable.columns:
        cols = ["periodo"] + cols

    view = timetable[timetable["profesor"] != ""]
    duplicated = view.groupby(cols).size().reset_index(name="count")
    return duplicated[duplicated["count"] > 1].sort_values(cols)


def detect_group_overlaps(timetable: pd.DataFrame) -> pd.DataFrame:
    """Detect group overlaps by group, day, and hour."""
    if timetable.empty or not {"grupo", "dia", "hora"}.issubset(timetable.columns):
        return pd.DataFrame(columns=["grupo", "dia", "hora", "count"])

    cols = ["grupo", "dia", "hora"]
    if "periodo" in timetable.columns:
        cols = ["periodo"] + cols

    duplicated = timetable.groupby(cols).size().reset_index(name="count")
    return duplicated[duplicated["count"] > 1].sort_values(cols)


def build_fixed_room_consistency(
    timetable: pd.DataFrame,
    alias_map: Dict[str, str],
) -> pd.DataFrame:
    """Evaluate whether each course-group pair uses a single room."""
    required_cols = {"materia", "grupo", "aula"}
    if timetable.empty or not required_cols.issubset(timetable.columns):
        return pd.DataFrame(
            columns=[
                "materia",
                "grupo",
                "room_count",
                "main_room",
                "main_room_sessions",
                "fixed_room_ok",
            ]
        )

    view = timetable.copy()
    view["aula"] = _clean_room_series(view["aula"])
    view = view[view["aula"] != ""]

    counts = (
        view.groupby(["materia", "grupo", "aula"])
        .size()
        .reset_index(name="sesiones")
    )
    top = (
        counts.sort_values(
            ["materia", "grupo", "sesiones"],
            ascending=[True, True, False],
        )
        .drop_duplicates(["materia", "grupo"])
    )
    distinct = (
        counts.groupby(["materia", "grupo"])["aula"]
        .nunique()
        .reset_index(name="room_count")
    )

    out = top.merge(distinct, on=["materia", "grupo"], how="left")
    out["fixed_room_ok"] = out["room_count"].eq(1)
    out["aula_alias"] = out["aula"].astype(str).str.upper().map(alias_map).fillna("")
    out = out.rename(
        columns={"aula": "main_room", "sesiones": "main_room_sessions"}
    )

    return out.sort_values(
        ["fixed_room_ok", "room_count", "materia", "grupo"],
        ascending=[True, False, True, True],
    ).reset_index(drop=True)


def build_phase2_coverage(meta: dict) -> pd.DataFrame:
    """Build a table describing per-course room whitelist sizes."""
    room_whitelist = (
        meta.get("ROOM_WHITELIST_BY_COURSE", {})
        or meta.get("room_whitelist_by_course", {})
        or {}
    )

    rows = []
    for key, rooms in room_whitelist.items():
        if "|" in str(key):
            course, group = str(key).split("|", 1)
        else:
            course, group = str(key), ""

        rooms = list(rooms or [])
        rows.append(
            {
                "materia": course,
                "grupo": group,
                "whitelist_room_count": len(rooms),
                "whitelist_rooms": ", ".join(rooms),
            }
        )

    return pd.DataFrame(rows)


def compare_two_timetables(
    current: pd.DataFrame,
    comparison: pd.DataFrame,
) -> Dict[str, pd.DataFrame]:
    """Compare two timetable files using assignment-level keys."""
    output: Dict[str, pd.DataFrame] = {}

    if current.empty or comparison.empty:
        return output

    keys = ["materia", "grupo", "dia", "hora", "profesor", "aula"]
    if not set(keys).issubset(current.columns) or not set(keys).issubset(comparison.columns):
        return output

    current_view = current[keys].drop_duplicates().copy()
    comparison_view = comparison[keys].drop_duplicates().copy()

    output["only_current"] = current_view.merge(
        comparison_view,
        on=keys,
        how="left",
        indicator=True,
    )
    output["only_current"] = output["only_current"][
        output["only_current"]["_merge"] == "left_only"
    ].drop(columns=["_merge"])

    output["only_comparison"] = comparison_view.merge(
        current_view,
        on=keys,
        how="left",
        indicator=True,
    )
    output["only_comparison"] = output["only_comparison"][
        output["only_comparison"]["_merge"] == "left_only"
    ].drop(columns=["_merge"])

    return output


def build_teacher_subjects(timetable: pd.DataFrame) -> pd.DataFrame:
    """Build a summary of courses and groups assigned to each teacher."""
    columns = [
        "profesor",
        "profesor_nombre",
        "course_count",
        "courses",
        "course_names",
        "group_count",
        "groups",
        "blocks",
        "rooms",
    ]

    if timetable.empty or "profesor" not in timetable.columns:
        return pd.DataFrame(columns=columns)

    view = timetable.copy()
    view = view[view["profesor"].astype(str).str.strip() != ""]

    if view.empty:
        return pd.DataFrame(columns=columns)

    rows = []
    for teacher, group in view.groupby("profesor"):
        rows.append(
            {
                "profesor": teacher,
                "profesor_nombre": (
                    _join_unique(group["profesor_nombre"])
                    if "profesor_nombre" in group.columns
                    else ""
                ),
                "course_count": (
                    group["materia"].nunique() if "materia" in group.columns else 0
                ),
                "courses": (
                    _join_unique(group["materia"]) if "materia" in group.columns else ""
                ),
                "course_names": (
                    _join_unique(group["materia_nombre"])
                    if "materia_nombre" in group.columns
                    else ""
                ),
                "group_count": (
                    group["grupo"].nunique() if "grupo" in group.columns else 0
                ),
                "groups": (
                    _join_unique(group["grupo"]) if "grupo" in group.columns else ""
                ),
                "blocks": len(group),
                "rooms": (
                    _join_unique(group["aula"]) if "aula" in group.columns else ""
                ),
            }
        )

    out = pd.DataFrame(rows)
    return out.sort_values(
        ["blocks", "course_count", "profesor_nombre", "profesor"],
        ascending=[False, False, True, True],
    ).reset_index(drop=True)


