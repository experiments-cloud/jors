def _candidate_suffixes(kind: str) -> List[str]:
    """Return output suffix candidates for the requested file type."""
    legacy = {
        "teachers": "_prof" + "esores.csv",
        "rooms": "_au" + "las.csv",
        "timetable": "_cal" + "endario.csv",
    }
    modern = {
        "teachers": "_teachers.csv",
        "rooms": "_rooms.csv",
        "timetable": "_timetable.csv",
    }
    return [modern[kind], legacy[kind]]


def discover_periods(template: str, suffix: str = "_timetable.csv") -> List[str]:
    """Discover period labels from files matching a period-aware template."""
    template = normalize_template(template)

    if not has_template(template):
        return []

    periods = set()
    for suffix_candidate in [suffix, *_candidate_suffixes("timetable")]:
        prefix_part, suffix_part = template.split("{period}", 1)
        pattern = f"{prefix_part}*{suffix_part}{suffix_candidate}"
        prefix_regex = re.escape(prefix_part.replace("\\", "/"))
        suffix_regex = re.escape(suffix_part.replace("\\", "/"))
        full_suffix_regex = re.escape(suffix_candidate)

        for file_name in glob.glob(pattern):
            normalized_file = file_name.replace("\\", "/")
            match = re.match(
                rf"^{prefix_regex}(.+){suffix_regex}{full_suffix_regex}$",
                normalized_file,
            )
            if match:
                periods.add(match.group(1))

    return sorted(periods)


def discover_prefixes(prefix_template: str) -> List[str]:
    """Discover available output prefixes from modern and legacy timetable files."""
    prefix_template = normalize_template(prefix_template)

    if has_template(prefix_template):
        prefix_part, suffix_part = prefix_template.split("{period}", 1)
        output_prefixes = []
        for suffix_candidate in _candidate_suffixes("timetable"):
            pattern = f"{prefix_part}*{suffix_part}{suffix_candidate}"
            for file_name in glob.glob(pattern):
                if file_name.endswith(suffix_candidate):
                    output_prefixes.append(file_name[: -len(suffix_candidate)])
        return sorted(set(output_prefixes))

    base = prefix_template.strip()
    if not base:
        return []

    output_prefixes = []
    for suffix_candidate in _candidate_suffixes("timetable"):
        for file_name in glob.glob(base + "*" + suffix_candidate):
            output_prefixes.append(file_name[: -len(suffix_candidate)])
        if os.path.isfile(base + suffix_candidate):
            output_prefixes.append(base)

    return sorted(set(output_prefixes))


